console.log("Dia da semana:");

let id = parseInt(prompt("Dia da semana:"));

console.log(id);

switch(id) {
    case 1:
        id = "Domingo";
        console.log(id);
        break;
    case 2:
        id = "Segunda";
        console.log(id);
        break;
    case 3:
        id = "Terça";
        console.log(id);
        break;
    case 4:
        id = "Quarta";
        console.log(id);
        break;
    case 5:
        id = "Quinta";
        console.log(id);
        break;
    case 6:
        id = "Sexta";
        console.log(id);
        break;
    case 7:
        id = "Sábado";
        console.log(id);
        break;
}